const BASE = import.meta.env.BASE_URL.replace(/\/$/, "");

type SiteStats = {
  founderCount: number;
  stat1Value: string;
  stat1Label: string;
  stat2Label: string;
  stat3Value: string;
  stat3Label: string;
  heroBadge: string;
};

const DEFAULT_STATS: SiteStats = {
  founderCount: 0,
  stat1Value: "$20M+",
  stat1Label: "Combined revenue generated by Startling Capital startups",
  stat2Label: "Founders in the startling-capital.community",
  stat3Value: "100+",
  stat3Label: "Black-owned STEM businesses launched",
  heroBadge: "Female-Led · Black Business Ecosystem · Built for Founders",
};

function useSiteStats() {
  const [stats, setStats] = useState<SiteStats>(DEFAULT_STATS);
  useEffect(() => {
    fetch(`${BASE}/api/public/stats`)
      .then(r => r.ok ? r.json() : null)
      .then(data => { if (data) setStats(data); })
      .catch(() => {});
  }, []);
  return stats;
}


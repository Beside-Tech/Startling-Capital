function usePublicTestimonials() {
  const [testimonials, setTestimonials] = useState<PublicTestimonial[]>([]);
  useEffect(() => {
    fetch(`${BASE}/api/public/testimonials`)
      .then(r => r.ok ? r.json() : [])
      .then(setTestimonials)
      .catch(() => {});
  }, []);
  return testimonials;
}

